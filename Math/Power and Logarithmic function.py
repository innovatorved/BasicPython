import math

#return exponential of given value e**x
print(math.exp(5))

#returns e**x-1
print(math.expm1(5))

#With one argument , returns the logarithm of x (to base 10)
print(math.log(10))

#With two arguments , it considers the given base for calculating log value
print(math.log(100,10))
       #log value of 100 has been calculated by considering the base 10
